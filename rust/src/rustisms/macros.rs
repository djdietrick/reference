// Essentially code that writes other code

// Declarative macros
#[macro_export]
macro_rules! vec {
    ( $( $x:expr ),* ) => {
        {
            let mut temp_vec = Vec::new();
            $(
                temp_vec.push($x);
            )*
            temp_vec
        }
    };
}

// macro_export makes the macro available anywhere that brings the crate into scope
